package com.company;
import java.util.*;
public class Conditional_Statements {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        //Check if number is odd or even
        int x = sc.nextInt();
        if(x % 2 == 0){
            System.out.println("Even");
        }
        else {
            System.out.println("Odd");
        }
        }
    }


